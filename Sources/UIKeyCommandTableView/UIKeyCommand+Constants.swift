import UIKit

// MARK: - Constants

public extension UIKeyCommand {
    /// Represents the Tab key input.
    static let inputTab = "\t"
    
    /// Represents the Return (Enter) key input.
    static let inputReturn = "\r"
    
    /// Represents the Backspace key input.
    static let inputBackspace = "\u{8}"
    
    /// Represents the Delete key input.
    static let inputDelete = "\u{7F}"
    
    /// Represents the Space key input.
    static let inputSpace = " "
}
